// Escape Notation
var str1 = "This is 'Escape'" // Output: This is 'Escape' 
console.log(str1)

var str2 = 'This is \'Escape\'' // Output: This is 'Escape' 
console.log(str2)

// New Line
var str3 = "This is \n'new line'" // Output: This is 
                                  //'new line' 
console.log(str3)

// Tab
var str4 = "This is \t'tab'" // Output: This is 	'tab' 
console.log(str4)

// Back Slash
var str5 = "This is \\'Back Slash'" // Output: This is 	'back Slash' 
console.log(str5)