// To find Present Date and Time
var date = new Date()
console.log(date); //2022-08-15T18:56:14.208Z
console.log(date.toDateString()); //Tue Aug 16 2022
console.log(date.toLocaleString()); //8/16/2022, 12:59:33 AM


console.log(date.getFullYear()); //2022
console.log(date.getMonth()); //7
console.log(date.getDate()); //16


console.log(date.getHours()); //1
console.log(date.getMinutes()); //2
console.log(date.getSeconds()); //50 

