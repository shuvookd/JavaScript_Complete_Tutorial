//Operator

/*
Arithmetic Operator(+,-,*,/, %, ++, --)
*/

var a = 11
var b = 3

console.log(a%b) //Modulus


 //Incremental

 /*
 1.Pre incremental (++a)/(--a)
 2.Post incremental(a++)/(a--)
 */
console.log(++a)  //a=(a+1)Pre Incremental

console.log(b++)  //First Time a = a Pre Incremental
console.log(b)    //Incrementing Value 2nd time    

/*
Assignment Operator(=)
*/
a+=b   //a = a+b 
console.log(a)

a-=b   //a = a-b 
console.log(a)


a*=b   //a = a*b 
console.log(a)

a/=b   //a = a/b 
console.log(a)

/*
Comparison Operator(==, !=, <,>)
*/
console.log(a==b)  //False
console.log(a!=b)  //True

console.log(a<b)  
console.log(a>b)

console.log(a<=b)  
console.log(a>=b)



var a = '10'
var b = 10

console.log(a==b)  //Only value Match
console.log(a===b) // value Match with type
console.log(a!==b) // value Match with type


